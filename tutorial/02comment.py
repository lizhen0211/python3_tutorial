# 注释
# Python中单行注释以 # 开头，例如：

# 这是一个注释
print("Hello, World!")

# 多行注释用三个单引号 ''' 或者三个双引号 """ 将注释括起来，例如:
# 1、单引号（'''）

# !/usr/bin/python3
'''
这是多行注释，用三个单引号
这是多行注释，用三个单引号 
这是多行注释，用三个单引号
'''
print("Hello, World!")
# =============================================================
# 2、双引号（"""）
# !/usr/bin/python3
"""
这是多行注释，用三个双引号
这是多行注释，用三个双引号 
这是多行注释，用三个双引号
"""
print("Hello, World!")
# =============================================================
